package com.cg.basicstwo;

public class LoopsforEachLoop {

	public static void main(String[] args) {
		// forEach loops
		// this loop iterates for each of the elements in a given range of elements from index one to length-1
		// example:
		String[] animals= {"Hamster", "Dog", "Cat", "Whale", "Platypus"};
		for(String animal:animals){
			System.out.println(animal);
		}
		// in this loop it goes through all the elements in the array animals and prints each one.

	}

}
