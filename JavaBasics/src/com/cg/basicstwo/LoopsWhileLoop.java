package com.cg.basicstwo;

public class LoopsWhileLoop {

	public static void main(String[] args) {
		// While loop compares an already initiated value, if true, it performs the task
		// in the task, we have to mention a change in the value, or it loops to infinity
		// format of while loop: while(comparison){task, incrementation}
		// example:
		int i=0;
		while(i<10) {
			System.out.println(i);
			i++;
		}
		// prints all numbers in between 0 to 10
		// loop iterates only if comparison is true

	}

}
