// Program to define Exceptions
package com.cg.exceptionhandling;

public abstract class CommonExceptions {

	// ArithmeticException:
		abstract void arithmeticException();
		
	// ArrayIndexOutOfBoundsException:
		abstract void arrayIndexOutOfBoundsException();
		
	// FileNotFoundException:
		abstract void fileNotFoundException();
		
	// IOException:
		abstract void iOException();
		
	// NullPointerException:
		abstract void nullPointerException();

	// NumberFormatException:
		abstract void numberFormatException();


}
